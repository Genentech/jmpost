make_ggplot_of_funs <- function() {
    ggplot() +
        geom_line(aes(x = .data$time, y = .data$median), data = long_fit_df, color = long_color) +
        geom_ribbon(aes(x = .data$time, ymin = .data$lower, ymax = .data$upper), data = long_fit_df, alpha = 0.3, color = long_color, fill = long_color) +
        geom_point(aes(x = .data$t, y = .data$y), data = long_obs_dfs, color = long_color) +
        geom_line(aes(x = .data$time, y = .data$median * 600), data = surv_fit_df, color = surv_color) +
        geom_ribbon(aes(x = .data$time, ymin = .data$lower * 600, ymax = .data$upper * 600), data = surv_fit_df, alpha = 0.3, color = surv_color, fill = surv_color) +
        theme_void()
}
