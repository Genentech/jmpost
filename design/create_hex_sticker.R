    require(showtext)

    hexSticker::sticker(
        subplot = make_ggplot_of_funs(),
        package = "jmpost",
        p_size = 140,
        p_color = pkg_color,
        p_y = 1.5,
        s_y = 1,
        s_x = 1,
        s_width = 1.8,
        s_height = 1,
        h_fill = fill_color,
        h_color = pkg_color,
        h_size = 2,
        url = "github.com/Genentech/jmpost",
        u_size = 24,
        u_color = pkg_color,
        filename = out_path,
        p_family = "mono",
        white_around_sticker = TRUE,
        dpi = 2000
    )
}

